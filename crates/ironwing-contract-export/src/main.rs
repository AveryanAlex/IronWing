fn operation_ids_ts() -> Result<String, Box<dyn Error>> {
    let mut body = String::from("export const OPERATION_IDS = [\n");
    for operation_id in ipc::OperationId::ALL {
        body.push_str("  ");
        body.push_str(&serde_json::to_string(operation_id.as_str())?);
        body.push_str(",\n");
    }
    body.push_str("] as const;\n");
    Ok(body)
}

fn semantic_config() -> specta_typescript::semantic::Configuration {
    specta_typescript::semantic::Configuration::default()
        .enable_lossless_bigints()
        .define::<serde_json::Value>(|_| unknown_datatype(), None, None)
        .define::<serde_json::Number>(|_| number_datatype(), None, None)
}

fn unknown_datatype() -> DataType {
    specta_typescript::Unknown::<()>::definition(&mut Types::default())
}

fn number_datatype() -> DataType {
    specta_typescript::Number::<()>::definition(&mut Types::default())
}

fn with_header(body: impl AsRef<str>) -> String {
    let mut output = String::from(HEADER);
    output.push_str(&trim_trailing_line_whitespace(body.as_ref()));
    if !output.ends_with('\n') {
        output.push('\n');
    }
    output
}

fn trim_trailing_line_whitespace(value: &str) -> String {
    value
        .lines()
        .map(str::trim_end)
        .collect::<Vec<_>>()
        .join("\n")
}
