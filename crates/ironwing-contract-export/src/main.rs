const HEADER: &str = "// @generated by `cargo run -p ironwing-contract-export --`\n// Do not edit this file by hand.\n";

fn main() {
    if let Err(error) = run() {
        eprintln!("{error}");
        process::exit(1);
    }
}

fn run() -> Result<(), Box<dyn Error>> {
    let check = env::args().skip(1).any(|arg| arg == "--check");
    let output_dir = generated_dir()?;
    let files = generated_files()?;

    if check {
        let mut changed = Vec::new();
        for (name, content) in &files {
            let path = output_dir.join(name);
            let current = fs::read_to_string(&path).unwrap_or_default();
            if current != *content {
                changed.push(path);
            }
        }

        if changed.is_empty() {
            println!("generated contract files are up to date");
            return Ok(());
        }

        eprintln!(
            "generated contract files are out of date; run `cargo run -p ironwing-contract-export --`"
        );
        for path in changed {
            eprintln!("  differs: {}", path.display());
        }
        process::exit(1);
    }

    fs::create_dir_all(&output_dir)?;
    for (name, content) in files {
        fs::write(output_dir.join(name), content)?;
    }
    println!("wrote generated contract files to {}", output_dir.display());

    Ok(())
}

fn generated_dir() -> Result<PathBuf, Box<dyn Error>> {
    let manifest_dir = PathBuf::from(env!("CARGO_MANIFEST_DIR"));
    let workspace_root = manifest_dir
        .parent()
        .and_then(|path| path.parent())
        .ok_or("failed to resolve workspace root from exporter manifest directory")?;
    Ok(workspace_root.join("src/lib/generated"))
}

fn generated_files() -> Result<Vec<(&'static str, String)>, Box<dyn Error>> {
    Ok(vec![
        ("mavkit.ts", generated_mavkit()?),
        ("ironwing.ts", generated_ironwing()?),
        ("events.ts", generated_events()?),
        ("commands.ts", generated_commands()?),
        ("ironwing-json.ts", generated_ironwing_json()),
        ("mavkit-json.ts", generated_mavkit_json()),
        ("index.ts", generated_index()),
    ])
}

